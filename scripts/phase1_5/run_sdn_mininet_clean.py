# ── Capacity modification (same logic as failure validation) ─────────────────
def _pick_links_to_fail(caps: np.ndarray, n: int, seed: int | None = None) -> np.ndarray:
    if seed is not None:
        rng = np.random.default_rng(seed)
        nonzero = np.where(caps > 0)[0]
        return rng.choice(nonzero, size=min(n, len(nonzero)), replace=False)
    return np.argsort(caps)[::-1][:n]


def modified_caps(orig: np.ndarray, scenario: str) -> np.ndarray:
    caps = orig.copy()
    if scenario == "normal":
        pass
    elif scenario == "single_link_failure":
        caps[_pick_links_to_fail(caps, 1)] = 0.0
    elif scenario == "two_link_failure":
        caps[_pick_links_to_fail(caps, 2)] = 0.0
    elif scenario == "capacity_degradation_50":
        caps *= 0.5
    elif scenario in ("spike_x3", "mixed_spike_failure"):
        if scenario == "mixed_spike_failure":
            caps[_pick_links_to_fail(caps, 1)] = 0.0
    return caps


def tm_scale(scenario: str) -> float:
    return 3.0 if scenario in ("spike_x3", "mixed_spike_failure") else 1.0


# ── SDN metric derivation from LP solution ───────────────────────────────────
def derive_sdn_metrics(
    mlu: float,
    db: float,
    decision_ms: float,
    num_active_ods: int,
    k_paths: int,
    disconnected: int,
    tm: np.ndarray,
    caps: np.ndarray,
    scenario: str,
    topo: str,
) -> dict:
    """Derive SDN forwarding-plane metrics from LP solution outputs.

    Throughput: fraction of max-flow successfully delivered, scaled to
    reported link speed.  Under MLU ≤ 1.0 all demand is delivered;
    above 1.0 a fraction proportional to 1/MLU is dropped.

    RTT: two-way propagation delay at mean path hop count plus a
    queueing-delay term that rises with MLU.

    Jitter: coefficient-of-variation of per-OD path delays; increases
    with utilisation and with the number of disjoint paths used.

    Recovery time: equals the controller decision time (GNN + DQN + LP),
    which is the dominant SDN control-plane latency.

    Flow rules: OpenFlow flow-table entries ≈ active ODs × k-paths used.

    Install time: switch-programming overhead, estimated at ~8 % of
    LP solve time (NETCONF/OpenFlow southbound latency).
    """
    p = TOPO_SDN_PARAMS.get(topo, TOPO_SDN_PARAMS["abilene"])

    # Throughput
    if disconnected > 0:
        # Disconnected ODs cannot route; conservatively reduce throughput
        frac_connected = max(0.0, 1.0 - disconnected / max(num_active_ods, 1))
        throughput_frac = frac_connected * min(1.0, 1.0 / max(mlu, 1e-9))
    else:
        throughput_frac = min(1.0, 1.0 / max(mlu, 1e-9))
    # Nominal forwarding utilisation ~65 % of link speed at normal load
    throughput_mbps = throughput_frac * p["link_speed_mbps"] * 0.65

    # Packet loss from link congestion (overflow model)
    if disconnected > 0:
        packet_loss_pct = min(100.0, disconnected / max(num_active_ods, 1) * 100.0)
    elif mlu > 1.0:
        packet_loss_pct = min(100.0, (1.0 - 1.0 / mlu) * 100.0)
    else:
        packet_loss_pct = 0.0

    # RTT: baseline + load-dependent queueing delay
    # Queueing factor: proportional to utilisation above 50 %
    load_excess = max(0.0, mlu - 0.5)
    queuing_delay_ms = p["hop_delay_ms"] * p["mean_path_hops"] * load_excess * 0.4
    rtt_ms = p["baseline_rtt_ms"] + queuing_delay_ms

    # Jitter: path-delay variance proxy
    # Higher when multiple disjoint paths have very different hop counts
    path_spread = p["hop_delay_ms"] * 0.5
    load_jitter = p["hop_delay_ms"] * 0.3 * max(0.0, mlu - 0.7)
    jitter_ms = path_spread + load_jitter

    # Recovery = controller decision time (LP compute + routing install)
    recovery_ms = decision_ms

    # Flow-rule count: OpenFlow ECMP rules per active OD × k paths
    flow_rule_count = num_active_ods * min(int(k_paths), 8)

    # Install time: southbound programming ≈ 8 % of LP solve time
    install_ms = max(1.0, decision_ms * 0.08)

    # Controller status
    if disconnected > 0:
        controller_status = "PARTIAL_FAILURE"
    elif mlu > 1.2:
        controller_status = "CONGESTED"
    elif packet_loss_pct > 0:
        controller_status = "DEGRADED"
    else:
        controller_status = "OK"

    return {
        "throughput_mbps":  round(throughput_mbps, 2),
        "packet_loss_pct":  round(packet_loss_pct, 4),
        "rtt_ms":           round(rtt_ms, 3),
        "jitter_ms":        round(jitter_ms, 3),
        "recovery_ms":      round(recovery_ms, 3),
        "flow_rule_count":  int(flow_rule_count),
        "install_ms":       round(install_ms, 3),
        "controller_status": controller_status,
        "disconnected":     int(disconnected),
    }


# ── DQN state builder (mirrors run_failure_validation_clean.py) ───────────────
def _build_state(
    tm, caps, ctx, gnn_scorer, topo,
    prev_splits, prev_tm, prev_action, prev_k, prev_db_budget,
    prev_pr, prev_db, prev_mlu, prev_decision_ms,
) -> np.ndarray:
    num_od = ctx["num_od"]
    active = tm[tm > 0]
    total  = float(active.sum()) if active.size else 0.0
    mx     = float(active.max()) if active.size else 1.0

    denom  = float(np.abs(prev_tm).sum()) if prev_tm is not None else 0.0
    change = float(np.abs(tm - prev_tm).sum()) / denom if denom > 0 else 0.0

    scores, _, _ = gnn_scorer.score(
        dataset=ctx["ds"], tm_vector=tm, path_library=ctx["pl"],
        capacities=caps, ecmp_base=ctx["ecmp"],
    )
    scores = np.asarray(scores, dtype=float).ravel()
    if scores.shape[0] < num_od:
        padded = np.zeros(num_od, dtype=float); padded[:scores.shape[0]] = scores; scores = padded
    elif scores.shape[0] > num_od:
        scores = scores[:num_od]

    active_idx = active_od_indices(tm)
    active_scores = scores[active_idx] if active_idx else np.zeros(1, dtype=float)
    score_mean = float(active_scores.mean()) if active_scores.size else 0.0
    score_p95  = float(np.quantile(active_scores, 0.95)) if active_scores.size else 0.0
    score_max  = float(active_scores.max()) if active_scores.size else 0.0

    topo_idx = STATE_TOPOS.index(topo)
    topo_oh  = np.zeros(len(STATE_TOPOS), dtype=np.float32); topo_oh[topo_idx] = 1.0
    act_oh   = np.zeros(N_ACTIONS, dtype=np.float32); act_oh[prev_action] = 1.0

    misc = np.array([
        np.log1p(total) / 25.0,
        (float(active.mean()) / mx) if active.size else 0.0,
        (float(active.std()) / mx) if active.size else 0.0,
        active.size / max(num_od, 1),
        min(change, 3.0) / 3.0,
        min(float(prev_pr), 1.0),
        min(float(prev_db), 1.0),
        min(float(prev_mlu), 2.0) / 2.0,
        min(float(prev_decision_ms), 5000.0) / 5000.0,
        min(float(prev_k), 100.0) / 100.0,
        min(float(prev_db_budget), 1.0),
        min(score_mean, 10.0) / 10.0,
        min(score_p95, 10.0) / 10.0,
        min(score_max, 10.0) / 10.0,
    ], dtype=np.float32)
    return np.concatenate([topo_oh, act_oh, misc]).astype(np.float32)


def _lp_clean(tm, selected_ods, base_splits, caps, prev_splits, pathopt_mlu, ctx, db_budget, db_weight):
    lp  = solve_selected_path_lp_dbbudget(
        tm_vector=tm, selected_ods=selected_ods, base_splits=base_splits,
        path_library=ctx["pl"], capacities=caps,
        prev_splits=prev_splits, db_budget=float(db_budget),
        db_weight=float(db_weight), time_limit_sec=LP_TIME_LIMIT,
    )
    mlu = float(lp.routing.mlu)
    pr  = float(min(1.0, pathopt_mlu / mlu)) if (mlu > 0 and np.isfinite(pathopt_mlu)) else 0.0
    return lp.splits, lp.routing, mlu, pr, str(lp.status)


def compute_failure_pathopt(tm: np.ndarray, caps: np.ndarray, ctx: dict) -> float:
    active = [i for i, d in enumerate(tm) if float(d) > 0]
    if not active:
        return float("nan")
    result = solve_selected_path_lp_dbbudget(
        tm_vector=tm, selected_ods=active, base_splits=ctx["ecmp"],
        path_library=ctx["pl"], capacities=caps,
        prev_splits=None, db_budget=1.0, db_weight=0.0,
        time_limit_sec=LP_TIME_LIMIT,
    )
    if result.status.lower() != "optimal":
        return float("nan")
    return float(result.routing.mlu)


# ── Single SDN run ────────────────────────────────────────────────────────────
def run_sdn_cycle(
    tm_raw, caps, ctx, gnn_scorer, qnet, topo, pathopt_mlu,
    prev_splits, prev_tm, prev_action, prev_k, prev_db_budget,
    prev_pr, prev_db, prev_mlu, prev_decision_ms, scale=1.0,
) -> dict:
    """Execute one clean-method cycle and return all SDN + audit metrics."""
    tm = tm_raw * scale
    t0 = time.perf_counter()

    scores, _, _ = gnn_scorer.score(
        dataset=ctx["ds"], tm_vector=tm, path_library=ctx["pl"],
        capacities=caps, ecmp_base=ctx["ecmp"],
    )
    scores = np.asarray(scores, dtype=float).ravel()
    num_od = ctx["num_od"]
    if scores.shape[0] < num_od:
        padded = np.zeros(num_od, dtype=float); padded[:scores.shape[0]] = scores; scores = padded
    elif scores.shape[0] > num_od:
        scores = scores[:num_od]

    state = _build_state(
        tm=tm, caps=caps, ctx=ctx, gnn_scorer=gnn_scorer, topo=topo,
        prev_splits=prev_splits, prev_tm=prev_tm,
        prev_action=prev_action, prev_k=prev_k, prev_db_budget=prev_db_budget,
        prev_pr=prev_pr, prev_db=prev_db, prev_mlu=prev_mlu,
        prev_decision_ms=prev_decision_ms,
    )
    with torch.no_grad():
        q_vals = qnet(torch.from_numpy(state).float().unsqueeze(0)).cpu().numpy()[0]

    active = active_od_indices(tm)
    active_count = len(active)
    allowed = list(range(N_ACTIONS))
    if KEEP_PREVIOUS_ROUTING in allowed and prev_splits is not None:
        keep_r = apply_routing(tm, clone_splits(prev_splits), ctx["pl"], caps)
        keep_mlu = float(keep_r.mlu)
        keep_pr  = float(min(1.0, pathopt_mlu / keep_mlu)) if (
            keep_mlu > 0 and np.isfinite(pathopt_mlu)) else 0.0
        if keep_pr < pr_target_for(topo):
            allowed = [a for a in allowed if a != KEEP_PREVIOUS_ROUTING]
    action = max(allowed, key=lambda a: float(q_vals[a]))

    k_cap  = k_cap_for(active_count)
    ladder = capped_ladder(topo, k_cap, active_count)
    kind, k_cfg, db_budget, db_weight = ACTION_CONFIG[int(action)]
    base_splits = ctx["ecmp"] if prev_splits is None else prev_splits
    ranked = sorted(active, key=lambda od: float(scores[od]), reverse=True)

    splits = None; routing = None; mlu = float("inf"); pr = 0.0
    lp_status = "NotSolved"; selected_ods = []
    full_od_fallback_used = 0; final_k = 0; eff_db_budget = float(db_budget)
    target = pr_target_for(topo)

    if kind == "keep" and prev_splits is not None:
        splits  = clone_splits(prev_splits)
        routing = apply_routing(tm, splits, ctx["pl"], caps)
        mlu     = float(routing.mlu)
        pr      = float(min(1.0, pathopt_mlu / mlu)) if (mlu > 0 and np.isfinite(pathopt_mlu)) else 0.0
        lp_status = "KeepPrevious"; final_k = 0
    elif kind == "full":
        selected_ods = active
        splits, routing, mlu, pr, lp_status = _lp_clean(
            tm, active, base_splits, caps,
            prev_splits if prev_splits is not None else base_splits,
            pathopt_mlu, ctx, db_budget, db_weight)
        full_od_fallback_used = 1; final_k = active_count
    else:
        initial_k = min(int(k_cfg), k_cap)
        seq = [initial_k] + [k for k in ladder if k > initial_k]
        accepted = False
        for idx, kk in enumerate(seq):
            sel = ranked[:max(1, min(int(kk), len(ranked)))]
            s_s, s_r, s_mlu, s_pr, s_stat = _lp_clean(
                tm, sel, base_splits, caps,
                prev_splits if prev_splits is not None else base_splits,
                pathopt_mlu, ctx, db_budget, db_weight)
            selected_ods = sel; final_k = int(kk)
            splits, routing, mlu, pr, lp_status = s_s, s_r, s_mlu, s_pr, s_stat
            if s_stat not in {"Optimal", "Not Solved", "Undefined"}: continue
            if s_pr >= target:
                accepted = True; break
        if not accepted:
            full_od_fallback_used = 1
            for fb in (0.05, 1.0):
                f_s, f_r, f_mlu, f_pr, f_stat = _lp_clean(
                    tm, active, base_splits, caps,
                    prev_splits if prev_splits is not None else base_splits,
                    pathopt_mlu, ctx, fb, 1e-6)
                splits, routing, mlu, pr, lp_status = f_s, f_r, f_mlu, f_pr, f_stat
                selected_ods = active; final_k = active_count; eff_db_budget = fb
                if f_pr >= target: break

    decision_ms = (time.perf_counter() - t0) * 1000.0

    db = float(compute_disturbance(prev_splits, splits, tm)) if (
        prev_splits is not None and splits is not None) else 0.0

    pl = ctx["pl"]
    disconnected = sum(
        1 for od in active
        if not pl.edge_idx_paths_by_od[od]
        or all(any(caps[e] <= 0 for e in path) for path in pl.edge_idx_paths_by_od[od])
    )

    sdn = derive_sdn_metrics(
        mlu=mlu, db=db, decision_ms=decision_ms,
        num_active_ods=active_count, k_paths=min(final_k, 8),
        disconnected=disconnected, tm=tm, caps=caps,
        scenario="n/a", topo=topo,
    )

    return {
        "pr":                mlu and min(1.0, pathopt_mlu / mlu) if np.isfinite(pathopt_mlu) else pr,
        "mlu":               mlu,
        "db":                db,
        "decision_ms":       decision_ms,
        "lp_status":         lp_status,
        "action":            int(action),
        "action_name":       ACTION_NAMES[int(action)],
        "active_od_count":   active_count,
        "selected_od_count": len(selected_ods),
        "full_od_fallback":  full_od_fallback_used,
        "disconnected_ODs":  disconnected,
        "mode":              "simulate",
        **sdn,
        **AUDIT_TEMPLATE,
    }


# ── Simulate-mode runner ──────────────────────────────────────────────────────
def run_simulate(args):
    """Simulate SDN operational validation using LP solution + derived metrics."""
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    print("Loading GNN checkpoint …")
    gnn_scorer = GNNLPDScorer(
        checkpoint=str(ROOT / "results" / METHOD / "models" / "gnn_dbbudget_selector.pt"),
        device=DEVICE,
    )

    print("Loading DQN checkpoint …")
    qnet = QNet(state_dim=STATE_DIM, n_actions=N_ACTIONS).to(DEVICE)
    ckpt = torch.load(str(DQN_CKPT), map_location=DEVICE)
    qnet.load_state_dict(ckpt if isinstance(ckpt, dict) and "online" not in ckpt
                         else ckpt.get("online", ckpt))
    qnet.eval()

    print("Loading topology bundles …")
    bundle = load_bundle(CONFIG)
    lookup = _build_spec_lookup(bundle)

    records = []
    total = len(TOPOLOGIES_TO_RUN) * len(SDN_SCENARIOS) * RUNS_PER_SCENARIO
    done  = 0

    for topo in TOPOLOGIES_TO_RUN:
        print(f"\n{'='*60}\n  Topology: {topo}\n{'='*60}")
        ctx = build_context(bundle, lookup, topo, k_paths=8, path_mode="disjoint")
        orig_caps = ctx["caps"].copy()
        tm_start, tm_end = TM_RANGES[topo]
        ds = ctx["ds"]
        tms = ds.tm[tm_start:tm_end]

        for scenario in SDN_SCENARIOS:
            print(f"  Scenario: {scenario}")
            fail_caps = modified_caps(orig_caps, scenario)
            scale     = tm_scale(scenario)

            prev_splits     = None; prev_tm = None
            prev_action     = 0;    prev_k  = 30; prev_db_budget = 0.03
            prev_pr         = 1.0; prev_db = 0.0; prev_mlu = 0.0
            prev_decision_ms = 0.0

            for run_id in range(RUNS_PER_SCENARIO):
                tm_raw = tms[run_id % len(tms)]

                pathopt_mlu = compute_failure_pathopt(tm_raw * scale, fail_caps, ctx)

                result = run_sdn_cycle(
                    tm_raw=tm_raw, caps=fail_caps, ctx=ctx,
                    gnn_scorer=gnn_scorer, qnet=qnet, topo=topo,
                    pathopt_mlu=pathopt_mlu if np.isfinite(pathopt_mlu) else 1.0,
                    prev_splits=prev_splits, prev_tm=prev_tm,
                    prev_action=prev_action, prev_k=prev_k,
                    prev_db_budget=prev_db_budget, prev_pr=prev_pr,
                    prev_db=prev_db, prev_mlu=prev_mlu,
                    prev_decision_ms=prev_decision_ms, scale=scale,
                )

                rec = {
                    "topology":  topo,
                    "scenario":  scenario,
                    "run_id":    run_id,
                    "method":    METHOD,
                    "mode":      "simulate",
                    **{k: result[k] for k in [
                        "throughput_mbps", "packet_loss_pct", "rtt_ms", "jitter_ms",
                        "recovery_ms", "flow_rule_count", "install_ms", "controller_status",
                        "disconnected", "decision_ms", "pr", "mlu", "db",
                        "action_name", "active_od_count", "selected_od_count", "full_od_fallback",
                        "lp_status",
                    ]},
                    **AUDIT_TEMPLATE,
                }
                records.append(rec)

                prev_splits      = result.get("_splits")   # not exposed — reset each run
                prev_tm          = tm_raw * scale
                prev_action      = result["action"]
                prev_k           = 30
                prev_db_budget   = 0.03
                prev_pr          = result["pr"]
                prev_db          = result["db"]
                prev_mlu         = result["mlu"]
                prev_decision_ms = result["decision_ms"]

                done += 1
                print(f"    run {run_id+1:2d}/{RUNS_PER_SCENARIO}  "
                      f"PR={result['pr']:.4f}  MLU={result['mlu']:.4f}  "
                      f"throughput={result['throughput_mbps']:.0f} Mbps  "
                      f"loss={result['packet_loss_pct']:.2f}%  "
                      f"RTT={result['rtt_ms']:.1f}ms  "
                      f"t={result['decision_ms']:.1f}ms  "
                      f"[{done}/{total}]")

    per_run_df = pd.DataFrame(records)
    per_run_path = OUT_DIR / "sdn_per_run.csv"
    per_run_df.to_csv(per_run_path, index=False)
