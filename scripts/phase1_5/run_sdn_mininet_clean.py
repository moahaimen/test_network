def _bar_figure(df: pd.DataFrame, metric: str, ylabel: str, title: str,
                fname: str, log: bool = False):
    topos = df["topology"].unique()
    scenarios = SDN_SCENARIOS
    nc = len(topos)
    fig, axes = plt.subplots(1, nc, figsize=(5 * nc, 4.5), sharey=False)
    if nc == 1:
        axes = [axes]
    for ax, topo in zip(axes, topos):
        sub = df[df["topology"] == topo].groupby("scenario")[metric].mean()
        vals = [sub.get(s, 0) for s in scenarios]
        labels = [_SC_LABELS.get(s, s) for s in scenarios]
        bars = ax.bar(labels, vals, color=_CLR[:len(vals)], edgecolor="white", linewidth=0.5)
        ax.set_title(topo.upper(), fontsize=11, fontweight="bold")
        ax.set_ylabel(ylabel if topos[0] == topo else "", fontsize=9)
        if log and all(v > 0 for v in vals):
            ax.set_yscale("log")
        ax.tick_params(axis="x", labelsize=8)
        ax.tick_params(axis="y", labelsize=8)
        ax.grid(axis="y", alpha=0.3)
    fig.suptitle(title, fontsize=12, fontweight="bold")
    plt.tight_layout()
    p = OUT_DIR / fname
    fig.savefig(str(p), dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"Written: {p}")


